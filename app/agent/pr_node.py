import os 
import uuid
from github import Github
from app.agent.state import AgentState

def _issue_number(messages):
    """Best-effort recovery of the issue number from the planner's tool call args."""
    for msg in messages:
        for call in getattr(msg, "tool_calls", None) or []:
            args = call.get("args", {}) if isinstance(call, dict) else {}
            if "issue_number" in args:
                return args["issue_number"]
    return "unknown"


def pr_node(state: AgentState):
    """Branches, commits the patched file, and opens a Pull Request.
        
       If the patch was approved (reviewer_feedback empty) -> normal PR.
       If logic retries were exhausted (reviewer_feedback still set) -> draft PR
       flagged for human review, with the reviewer's objection in the body.
    """

    repo_name = state.get("repo_name", "")
    file_path = state.get("file_path", "")
    patched = state.get("patched_code", "")
    issue_no = _issue_number(state.get("messages", []))
    rejection = state.get("reviewer_feedback", "")
    is_draft = bool(rejection)

    if not repo_name or not file_path or not patched:
        return {
            "status": "failed_pr",
            "pr_url": "",
            "verifier_feedback": "Missing repo_name/file_path/patched_code for PR."
        }

    token = os.getenv("GITHUB_ACCESS_TOKEN")
    gh = Github(token) if token else Github()

    try:
        repo = gh.get_repo(repo_name)
        default_branch = repo.default_branch
        base = repo.get_branch(default_branch)

        branch_name = f"ai-fix/issue-{issue_no}-{uuid.uuid4().hex[:6]}"
        repo.create_git_ref(ref=f"refs/heads/{branch_name}", sha=base.commit.sha)

        existing = repo.get_contents(file_path, ref=branch_name)
        commit_msg = f"fix: resolve issue #{issue_no} via AI SWE agent"
        repo.update_file(
            path=file_path,
            message=commit_msg,
            content=patched,
            sha=existing.sha,
            branch=branch_name
        )

        if is_draft:
            title = f"[DRAFT - needs human review] AI fix attempt for issue  #{issue_no}"
            body = (
                f"⚠️ **This patch did NOT pass the logical reviewer** and is opened as a "
                f"DRAFT for human review.\n\n"
                f"Automated patch for issue #{issue_no}. It passed the syntax verifier but "
                f"the reviewer rejected it after exhausting the logical-rewrite retry budget.\n\n"
                f"**Reviewer's objection:**\n> {rejection}\n\n"
                f"Please review carefully before merging."
            )
        else:
            title = f"AI fix for issue #{issue_no}"
            body = (
                f"Automated patch generated by the AI SWE agent for issue #{issue_no}.\n\n"
                "Passed the syntax verifier and the logical reviewer gates."
            )
        pr = repo.create_pull(
            title=title,
            body=body,
            head=branch_name,
            base=default_branch,
            draft=is_draft
        )
        return {
            "status": "pr_created_draft" if is_draft else "pr_created",
            "pr_url": pr.html_url
        }
       
    except Exception as e:
        return {
            "status":"failed_pr",
            "pr_url": "",
            "verifier_feedback": f"PR error: {e}"
        }